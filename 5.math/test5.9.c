//入门题库->5.数学问题->9.爱因斯坦
/*9.爱因斯坦：有一条长阶梯，若每步跨2阶，最后剩下1阶；
若每步跨3阶，最后剩下2阶；若每步跨5阶，最后剩下4阶；
若每步跨6阶，最后剩下5阶；只有每步跨7阶，最后才正好1阶不剩。计算这条阶梯共有多少阶。
*/
#include <stdio.h>
void print_level()
{
    int x;
    for (x = 1;; x++)
    {
        if (x % 2 == 1 && x % 3 == 2 && x % 5 == 4 && x % 6 == 5 && x % 7 == 0)
        {
            printf("%d", x);
            break;
        }
    }
}
void main()
{
    print_level();
}
